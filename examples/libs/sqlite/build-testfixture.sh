    "$SQLITE_FULL/src/test_demovfs.c"
    "$SQLITE_FULL/src/test_devsym.c"
    "$SQLITE_FULL/src/test_fs.c"
    "$SQLITE_FULL/src/test_func.c"
    "$SQLITE_FULL/src/test_hexio.c"
    "$SQLITE_FULL/src/test_init.c"
    "$SQLITE_FULL/src/test_intarray.c"
    "$SQLITE_FULL/src/test_journal.c"
    "$SQLITE_FULL/src/test_malloc.c"
    "$SQLITE_FULL/src/test_md5.c"
    "$SQLITE_FULL/src/test_multiplex.c"
    "$SQLITE_FULL/src/test_mutex.c"
    "$SQLITE_FULL/src/test_onefile.c"
    "$SQLITE_FULL/src/test_osinst.c"
    "$SQLITE_FULL/src/test_pcache.c"
    "$SQLITE_FULL/src/test_quota.c"
    "$SQLITE_FULL/src/test_rtree.c"
    "$SQLITE_FULL/src/test_schema.c"
    "$SQLITE_FULL/src/test_superlock.c"
    # test_syscall.c — excluded, overrides VFS syscalls
    "$SQLITE_FULL/src/test_tclsh.c"
    "$SQLITE_FULL/src/test_tclvar.c"
    # test_thread.c — excluded, requires pthreads
    "$SQLITE_FULL/src/test_vdbecov.c"
    "$SQLITE_FULL/src/test_vfs.c"
    "$SQLITE_FULL/src/test_window.c"
    "$SQLITE_FULL/src/test_wsd.c"
    # Extension test files
    "$SQLITE_FULL/ext/fts3/fts3_term.c"
    "$SQLITE_FULL/ext/fts3/fts3_test.c"
    "$SQLITE_FULL/ext/session/test_session.c"
    "$SQLITE_FULL/ext/recover/sqlite3recover.c"
    "$SQLITE_FULL/ext/recover/dbdata.c"
