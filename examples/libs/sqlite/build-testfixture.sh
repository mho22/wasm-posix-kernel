    "$SQLITE_FULL/ext/intck/test_intck.c"
    "$SQLITE_FULL/ext/intck/sqlite3intck.c"
    "$SQLITE_FULL/ext/rbu/test_rbu.c"
    # Statically linked extensions
    "$SQLITE_FULL/ext/expert/sqlite3expert.c"
    "$SQLITE_FULL/ext/expert/test_expert.c"
    "$SQLITE_FULL/ext/misc/amatch.c"
    "$SQLITE_FULL/ext/misc/appendvfs.c"
    "$SQLITE_FULL/ext/misc/basexx.c"
    "$SQLITE_FULL/ext/misc/carray.c"
