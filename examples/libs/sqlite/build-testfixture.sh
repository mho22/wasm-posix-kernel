    "$SQLITE_FULL/ext/misc/closure.c"
    "$SQLITE_FULL/ext/misc/csv.c"
    "$SQLITE_FULL/ext/misc/decimal.c"
    "$SQLITE_FULL/ext/misc/eval.c"
    "$SQLITE_FULL/ext/misc/explain.c"
    "$SQLITE_FULL/ext/misc/fileio.c"
    "$SQLITE_FULL/ext/misc/fuzzer.c"
    "$SQLITE_FULL/ext/fts5/fts5_tcl.c"
    "$SQLITE_FULL/ext/fts5/fts5_test_mi.c"
    "$SQLITE_FULL/ext/fts5/fts5_test_tok.c"
    "$SQLITE_FULL/ext/misc/ieee754.c"
    "$SQLITE_FULL/ext/misc/mmapwarm.c"
    "$SQLITE_FULL/ext/misc/nextchar.c"
    "$SQLITE_FULL/ext/misc/normalize.c"
    "$SQLITE_FULL/ext/misc/percentile.c"
    "$SQLITE_FULL/ext/misc/prefixes.c"
    "$SQLITE_FULL/ext/misc/qpvtab.c"
    "$SQLITE_FULL/ext/misc/randomjson.c"
    "$SQLITE_FULL/ext/misc/regexp.c"
    "$SQLITE_FULL/ext/misc/remember.c"
    "$SQLITE_FULL/ext/misc/series.c"
    "$SQLITE_FULL/ext/misc/spellfix.c"
    "$SQLITE_FULL/ext/misc/stmtrand.c"
    "$SQLITE_FULL/ext/misc/totype.c"
    "$SQLITE_FULL/ext/misc/unionvtab.c"
    "$SQLITE_FULL/ext/misc/wholenumber.c"
    "$SQLITE_FULL/ext/misc/zipfile.c"
    "$SQLITE_FULL/ext/rtree/test_rtreedoc.c"
)

# --- Compile all source files to .o ---
# Compile the amalgamation with SQLITE_PRIVATE= (empty) so that internal
# functions are visible to the test files (which reference them as extern).
# Without this, the amalgamation defines them as static.
echo "==> Compiling SQLite amalgamation..."
wasm32posix-cc -c "${CFLAGS[@]}" -DSQLITE_PRIVATE= "$SQLITE_SRC/sqlite3.c" -o sqlite3.o

echo "==> Compiling tclsqlite.c..."
wasm32posix-cc -c "${CFLAGS[@]}" "$SQLITE_FULL/src/tclsqlite.c" -o tclsqlite.o

echo "==> Compiling test source files (${#TESTSRC_FILES[@]} files)..."
OBJ_FILES=(sqlite3.o tclsqlite.o)
NPROC="$(sysctl -n hw.ncpu 2>/dev/null || nproc)"
COMPILE_JOBS=0

for src in "${TESTSRC_FILES[@]}"; do
    base="$(basename "$src" .c)"
    # Handle name collisions from different directories
    dir="$(basename "$(dirname "$src")")"
    obj="${dir}_${base}.o"
    wasm32posix-cc -c "${CFLAGS[@]}" "$src" -o "$obj" &
    OBJ_FILES+=("$obj")
    COMPILE_JOBS=$((COMPILE_JOBS + 1))
    if [ "$COMPILE_JOBS" -ge "$NPROC" ]; then
        wait
        COMPILE_JOBS=0
    fi
done
wait

# Compile stubs for excluded test modules
echo "==> Compiling wasm stubs..."
wasm32posix-cc -c "${CFLAGS[@]}" "$SCRIPT_DIR/wasm_stubs.c" -o wasm_stubs.o
OBJ_FILES+=(wasm_stubs.o)

echo "==> Linking testfixture (${#OBJ_FILES[@]} object files)..."
wasm32posix-cc "${CFLAGS[@]}" \
    "${OBJ_FILES[@]}" \
    -L"$TCL_INSTALL/lib" -ltcl8.6 \
    -L"$ZLIB_INSTALL/lib" -lz \
    -o testfixture

if [ ! -f testfixture ]; then
    echo "ERROR: Link failed — testfixture not found" >&2
    exit 1
fi

# --- Asyncify for fork support ---
# TCL's exec command uses fork(). Many SQLite tests use exec.
WASM_OPT="$(command -v wasm-opt 2>/dev/null || true)"
if [ -n "$WASM_OPT" ]; then
    echo "==> Applying asyncify transform..."
    "$WASM_OPT" --asyncify \
        --pass-arg="asyncify-imports@kernel.kernel_fork" \
        testfixture -o testfixture
else
    echo "WARNING: wasm-opt not found. Fork (exec command) will not work." >&2
fi

# --- Install ---
mkdir -p "$SCRIPT_DIR/bin"
cp testfixture "$SCRIPT_DIR/bin/testfixture.wasm"

echo "==> testfixture built successfully!"
ls -lh "$SCRIPT_DIR/bin/testfixture.wasm"
echo ""
echo "Run a test with:"
echo "  TCL_LIBRARY=\$TCL_INSTALL/lib/tcl8.6 KERNEL_CWD=\$SQLITE_FULL \\"
echo "    npx tsx examples/run-example.ts testfixture test/select1.test"
